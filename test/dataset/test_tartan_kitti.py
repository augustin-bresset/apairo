import os
import numpy as np
import unittest
from src.dataset.tartan_kitti import TartanKittiDataset
from src.loader import NPYLoader

from test.paths import tmp_path
from test.utils import (
    empty_dir, 
    create_random_images, create_random_npy_files,
    create_random_npy_file, create_timestamps_file
)

class TestTartanKittiDataset(unittest.TestCase):
    r"""Test the :class:`TartanKittiDataset` class.
    
    This contains the following kinds of data: npy, multiple npy, multiple images.
    """
    
    def setUp(self):
        self.directory = os.path.join(tmp_path, "tartan_kitti_test")
        
        # Simulate the data
        end_of_time = 10.0
        # npy format : controls with a frequency of 50Hz
        controls_dir = os.path.join(self.directory, "controls")
        freq = 50
        nb_msgs = int(freq * end_of_time)
        create_random_npy_file(nb_msgs , 2, "controls", controls_dir)
        create_timestamps_file(controls_dir, 0.0, end_of_time, nb_msgs, 0.01/freq)

        # multiple npy format : height_map with a frequency of 1 Hz
        height_map_dir = os.path.join(self.directory, "height_map")
        freq = 1
        nb_msgs = int(freq * end_of_time)
        create_random_npy_files(nb_msgs, (5, 5), height_map_dir)
        create_timestamps_file(height_map_dir, 0.0, end_of_time, nb_msgs, 0.01/freq)

        # multiple images : image_left with a frequency of 15 Hz
        image_left_dir = os.path.join(self.directory, "image_left")
        freq = 15
        nb_msgs = int(freq * end_of_time)
        create_random_images(nb_msgs, (16, 16), image_left_dir)
        create_timestamps_file(image_left_dir, 0.0, end_of_time, nb_msgs, 0.01/freq)

        self.dataset = TartanKittiDataset(
            directory=self.directory,
            keys=["controls", "height_map", "image_left"]
            )

    def test_set_keys(self):
        """Check if the keys are correctly set"""
        list_keys = [["controls"], ["height_map", "image_left"]]
        for keys in list_keys:
            self.dataset.keys = keys
            self.assertEqual(self.dataset.keys, keys)
            self.assertEqual(list(self.dataset.loaders.keys()), keys)
            self.assertEqual(list(self.dataset.timestamps.keys()), keys)
            

    def test_set_keys_iter(self):
        """Check if when setting the keys the iterator is correctly set"""
        self.dataset.keys = ["controls"]
        for key, _ in self.dataset:
            self.assertEqual(key, "controls")

    def test_timelines(self):
        """Check if the timeline go from past to future"""
        is_empty = False
        for key in self.dataset.keys:
            is_empty = True
            self.assertTrue(np.all(np.diff(self.dataset.timestamps[key]) >= 0))
        assert is_empty, "No key was tested"

    def test_time_order(self):
        """Check if the timeline generated by the iterable go from past to future"""
        
        last_time = 0.0
        for key, index in self.dataset:
            time = self.dataset.timestamps[key][index]
            if last_time > time:
                print(f"Last time: {last_time}, Time: {time}")
                print(f"Key: {key}, Index: {index}")
            self.assertTrue(time >= last_time)
            last_time = time

    def test_length(self):
        """Check if the length of the dataset is correct"""
        self.dataset.keys = ["controls"]
        controls = NPYLoader(os.path.join(self.directory, "controls"))
        self.assertEqual(len(list(self.dataset)), len(controls))


    def test_iter_ending(self):
        """Check the timelines to see if all the data is loaded"""
        self.dataset.keys = ["controls", "image_left"]
        
        for key, index in self.dataset:
            self.assertTrue(self.dataset.timelines[self.dataset.k2idx[key]] <= self.dataset.end_of_time)
        self.assertTrue(np.allclose(self.dataset.indexes, [len(self.dataset.timestamps[key]) for key in self.dataset.keys]))

    def test_iter_concrete(self):
        """Check if the iteration is correct for a concrete example"""

        self.dataset._keys = ["controls", "image_left"]
        self.dataset.timestamps = {
            "controls": np.array([0.0, .5, 1.0, 1.5, 2.0]),
            "image_left": np.array([0.0, 1.0, 2.0])
        }
        self.k2idx = {key: i for i, key in enumerate(self.dataset.keys)}
        true_timeline = [0.0, 0.0, 0.5, 1.0, 1.0, 1.5, 2.0, 2.0]
        self.dataset.end_of_time = 3.0
        for i, (key, index) in enumerate(self.dataset):
            self.assertEqual(self.dataset.timestamps[key][index], true_timeline[i])

    def tearDown(self):
        empty_dir(tmp_path)

if __name__ == "__main__":
    unittest.main()
