@pytest.fixture
def dataset(tartan_data):
    return TartanKittiDataset(
        directory=str(tartan_data),
        keys=["controls", "height_map", "image_left"]
    )


def test_set_keys(dataset):
    """Check if the keys are correctly set"""
    list_keys = [["controls"], ["height_map", "image_left"]]
    for keys in list_keys:
        dataset.keys = keys
        assert dataset.keys == keys
        assert list(dataset.loaders.keys()) == keys
        assert list(dataset.timestamps.keys()) == keys


def test_set_keys_iter(dataset):
    """Check if when setting the keys the iterator is correctly set"""
    dataset.keys = ["controls"]
    for sample in dataset:
        assert sample.key == "controls"


def test_timelines(dataset):
    """Check if the timeline go from past to future"""
    is_empty = False
    for key in dataset.keys:
        is_empty = True
        assert np.all(np.diff(dataset.timestamps[key]) >= 0)
    assert is_empty, "No key was tested"


def test_time_order(dataset):
    """Check if the timeline generated by the iterable go from past to future"""
    last_time = 0.0
    for sample in dataset:
        assert isinstance(sample, Sample)
        if last_time > sample.timestamp:
            print(f"Last time: {last_time}, Time: {sample.timestamp}")
            print(f"Key: {sample.key}")
        assert sample.timestamp >= last_time
        last_time = sample.timestamp


def test_length(dataset, tartan_data):
    """Check if the length of the dataset is correct"""
    dataset.keys = ["controls"]
    controls = NPYLoader(str(tartan_data / "controls"))
    assert len(list(dataset)) == len(controls)


def test_iter_ending(dataset):
    """Check the timelines to see if all the data is loaded"""
    dataset.keys = ["controls", "image_left"]

    for sample in dataset:
        assert isinstance(sample, Sample)
        assert sample.key in ["controls", "image_left"]


def test_iter_concrete():
    """Check if the iteration is correct for a concrete example"""
    dataset = TartanKittiDataset.__new__(TartanKittiDataset)  # Skip init
    dataset._keys = ["controls", "image_left"]
    dataset.timestamps = {
        "controls": np.array([0.0, .5, 1.0, 1.5, 2.0]),
        "image_left": np.array([0.0, 1.0, 2.0])
    }
    dataset.end_of_time = 3.0
    dataset._init_timeline()

    true_timeline = [0.0, 0.0, 0.5, 1.0, 1.0, 1.5, 2.0, 2.0]

    # Verify the numpy timeline arrays produce the expected timestamp order
    for i in range(len(dataset._tl_key_idxs)):
        key = dataset._keys[dataset._tl_key_idxs[i]]
        frame = int(dataset._tl_frame_idxs[i])
        assert dataset.timestamps[key][frame] == true_timeline[i]
